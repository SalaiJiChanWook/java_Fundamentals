
package Basic.Opreations;

public class AssigimentOpreator {
    public static void main(String[] args) {
        int x = 13;
        x+=2;
        System.out.println(x);
        
        int y = 14;
        y -= 2;
        System.out.println(y);
        
        x= y;
        System.out.println(x);
        
        x *= y;
        System.out.println(x);
        
        y/=x;
        System.out.println(y);
        
        x%=y;
        System.out.println(x);
    }
}
