
package Loopings;


public class Do_WhileLoop {
    public static void main(String[] args) {
        int i =0;
        do{
            System.out.println(i);
            i++;
        }while(i<0);
        int j = 0;
        while(j<0){
            System.out.println(j);
           
        }
    }
}
